function handleButtons(linkto) {
	switch (linkto) {
		case "youtube":
			window.open("https://youtube.com/nikander100", "_blank");
			break;
		case "github":
			window.open("https://github.com/nikander100", "_blank");
			break;
		case "twitch":
			window.open("https://www.twitch.tv/nikander100", "_blank");
			break;
		case "instagram":
			window.open("https://www.instagram.com/nikander100/", "_blank");
			break;
		case "discord":
			window.open("https://discord.gg/xekggu8phq", "_blank");
			break;
		case "reddit":
			window.open("https://www.reddit.com/r/nikander100", "_blank");
			break;
		case "ko-fi":
			window.open("https://ko-fi.com/nikander100", "_blank");
			break;
		case "codam":
			window.open("https://www.codam.nl/", "_blank");
			break;
	}
}
